	function readMore1(){
		let metus1 = document.getElementById("metus1");
		let show1 = document.getElementById("show1");
		let btn1 = document.getElementById("btn1");

		if  (metus1.style.display ==="none"){
			metus1.style.display="inline";
			btn1.innerHTML="Read More..."
			show1.style.display="none";
		}
		else{
			metus1.style.display="none";
			btn1.innerHTML="Hide"
			show1.style.display="inline";
		}
	}
